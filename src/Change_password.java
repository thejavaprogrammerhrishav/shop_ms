    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sql="select * from user where username='"+username.getText()+"' and emailid='"+emailid.getText()+"'";
        try{
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){
                pan.setVisible(true);
            }
            else
                JOptionPane.showMessageDialog(null, "No Such User Found");
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(password.getText().equals(con_password.getText())){
            String sql="update user set password='"+password.getText()+"' where username='"+username.getText()+"' and emailid='"+emailid.getText()+"'";
            try{
                pst=conn.prepareStatement(sql);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Password Changed Sucessfully");
            }catch(Exception ex){
                ex.printStackTrace();
            }
        }
        else
            JOptionPane.showMessageDialog(null, "Password DoesNot Match");
    }//GEN-LAST:event_jButton3ActionPerformed

