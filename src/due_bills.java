    private void set_value(){
        String sql="select * from purchase";
        try{
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            model.setRowCount(0);
            while(rs.next()){
                if(rs.getString("due").equalsIgnoreCase("true"))
                    model.addRow(new Object[] {rs.getString("cus_name"),rs.getString("invoice_no"),rs.getString("net_amount"),rs.getString("due")});
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
        tab.setModel(model);
    }

