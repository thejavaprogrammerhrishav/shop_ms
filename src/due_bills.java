    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String sql=null;
        if(tab.getSelectedRow()==-1)
            JOptionPane.showMessageDialog(null, "No Row Selected");
        else{
            if(status.getSelectedItem().equals("Select"))
                JOptionPane.showMessageDialog(null, "Invalid Status");
            else{
                if(status.getSelectedItem().equals("Paid"))
                    sql="update purchase set due='false' where invoice_no='"+tab.getValueAt(tab.getSelectedRow(), 1)+"'";
                if(status.getSelectedItem().equals("Due"))
                    sql="update purchase set due='true' where invoice_no='"+tab.getValueAt(tab.getSelectedRow(), 1)+"'";
            }
            try{
                pst=conn.prepareStatement(sql);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Updated Sucessfully");
            }catch(Exception ex){
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed


