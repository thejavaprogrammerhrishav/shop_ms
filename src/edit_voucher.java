    private void OPENActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OPENActionPerformed
        // TODO add your handling code here:
        if(all_voucher.getSelectedRow()==-1)
            JOptionPane.showMessageDialog(null, "No Row Selected");
        else{
            String sql="select * from voucher where voucher_number='"+all_voucher.getValueAt(all_voucher.getSelectedRow(), 2)+"'";
            String SQL="select * from voucher_contents where voucher_number='"+all_voucher.getValueAt(all_voucher.getSelectedRow(), 2)+"'";
            try{
                pst=conn.prepareStatement(sql);
                rs=pst.executeQuery();
                model.setRowCount(0);
                if(rs.next()){
                    customer.setText(rs.getString("name"));
                    date.setText(rs.getString("date"));
                    sub_total.setText(rs.getString("sub_total"));
                    total_tax.setText(rs.getString("total_tax"));
                    net_amount.setText(rs.getString("net_amount"));
                    pst=conn.prepareStatement(SQL);
                    voucher=rs.getString("voucher_number");
                    rs=pst.executeQuery();
                    while(rs.next()){
                        model.addRow(new Object[] {rs.getString("sl_no"),rs.getString("item_name"),rs.getString("rate"),rs.getString("GST"),
                        rs.getString("qty"),rs.getString("total")});
                    }
                }
            }catch(Exception ex){
                ex.printStackTrace();
            }
            ADD.setEnabled(true);
            EDIT.setEnabled(true);
            DELETE.setEnabled(true);
        }
    }//GEN-LAST:event_OPENActionPerformed

