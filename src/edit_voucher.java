    private void CALCULATEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CALCULATEActionPerformed
        // TODO add your handling code here:
        double sub=0.00,tax=0.00,nett=0.00;
        for(int i=0;i<open_voucher.getRowCount();i++){
            sub+=Double.parseDouble(open_voucher.getValueAt(i, 2).toString());
            tax+=Double.parseDouble(open_voucher.getValueAt(i, 3).toString());
            nett+=Double.parseDouble(open_voucher.getValueAt(i, 5).toString());
        }
        sub_total.setText(""+sub);
        total_tax.setText(""+tax);
        net_amount.setText(""+nett);
    }//GEN-LAST:event_CALCULATEActionPerformed

    private void ADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ADDActionPerformed
        // TODO add your handling code here:
        String item=JOptionPane.showInputDialog("Enter Item Name");
        if(item.equals(""))
            JOptionPane.showMessageDialog(null, "Enter A Valid Item");
        else{
            String sql="select * from product where item_name='"+item+"'";
            try{
                pst=conn.prepareStatement(sql);
                rs=pst.executeQuery();
                if(rs.next()){
                    String x=JOptionPane.showInputDialog("Enter Quantity");
                    double y=Double.parseDouble(rs.getString("Total_MRP"))*Double.parseDouble(x);
                    int a=(Integer.parseInt(open_voucher.getValueAt(open_voucher.getRowCount()-1, 0).toString())+1);
                    model.addRow(new Object[] {""+a,rs.getString("item_name"),rs.getString("rate"),rs.getString("GST"),x,y});
                }
                else
                    JOptionPane.showMessageDialog(null, "No Such Item Found");
            }catch(Exception ex){
                ex.printStackTrace();
            }
            open_voucher.setModel(model);
        }
    }//GEN-LAST:event_ADDActionPerformed

