    private void save_and_printActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_and_printActionPerformed
        // TODO add your handling code here:
        if(pay_mode.getSelectedItem().toString().equals("Select"))
            JOptionPane.showMessageDialog(null, "Select A Valid Payment Mode");
        else{
            String SQL="INSERT INTO payment(cus_name, date, invoice_no, total_amount, total_tax, discount, net_amount, "
                +"amount_paid, balance, pay_slip_no ,pay_mode, pay_date) "
                +"VALUES ('"+cus_name.getText()+"','"+date.getText()+"','"+invoice_number.getText()+"','"+total_amount.getText()+"','"
                +total_tax.getText()+"','"+discount.getText()+"','"+net_amount.getText()+"','"+amount_paid.getText()+"','"+balance.getText()
                +"','"+pay_slip_no.getText()+"','"+pay_mode.getSelectedItem().toString()+"','"+new SimpleDateFormat("yyyy-MM-dd").format(new Date())+"')";
            String s="update purchase set due='false' where invoice_no='"+invoice_number.getText()+"'";
            try{
                pst=conn.prepareStatement(SQL);
                pst.execute();
                pst=conn.prepareStatement(s);
                pst.executeUpdate();
                if(JOptionPane.showConfirmDialog(null, "Payment Slip Saved Sucessfully \n Press Yes To Print Payment Slip \n Warning: Afterwards Payment Slip Cannot Be Printed")==JOptionPane.YES_OPTION){
                    JasperDesign jd=JRXmlLoader.load(getClass().getResourceAsStream("/pay_slip.jrxml"));
                    JasperReport jp=JasperCompileManager.compileReport(jd);
                    Map mp=new HashMap();
                    mp.put("inv_no", invoice_number.getText());
                    JasperPrint jpp=JasperFillManager.fillReport(jp, mp, conn);
                    JasperViewer.viewReport(jpp, false);
                    JRViewer jr=new JRViewer(jpp);
                    Component[] comp=jr.getComponents();
                    for(Component c:comp){
                        System.out.println(c.getClass().getName());
                    }
                }
            }catch(HeadlessException | SQLException | JRException ex){
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_save_and_printActionPerformed

