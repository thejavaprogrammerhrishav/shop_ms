    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        ResultSet Rs;
        String sql="select * from purchase where invoice_no='"+inv_no.getText()+"'";
        try{
            pst=conn.prepareStatement(sql);
            Rs=pst.executeQuery();
            mod.removeAllElements();
            if(Rs.next()){
                mod.addElement(Rs.getString("invoice_no"));
            }
            else
                JOptionPane.showMessageDialog(null, "Not Found");
        }catch(Exception ex){
            ex.printStackTrace();
        }
        bill_list.setModel(mod);
    }//GEN-LAST:event_searchActionPerformed

    private void openActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openActionPerformed
        // TODO add your handling code here:
        if(bill_list.getSelectedIndex()==-1)
            JOptionPane.showMessageDialog(null, "No Invoice Number Selected");
        else{
            ResultSet RS;
            String sql="select * from purchase where invoice_no='"+bill_list.getSelectedValue()+"'";
            String SQL="select * from purchase_content where invoice_no='"+bill_list.getSelectedValue()+"'";
            try{
                pst=conn.prepareStatement(sql);
                Rs=pst.executeQuery();
                if(Rs.next()){
                    cus_name.setText(Rs.getString("cus_name"));
                    date.setText(new SimpleDateFormat("dd-MM-yyyy").format(Rs.getDate("date")));
                    invoice_number.setText(Rs.getString("invoice_no"));
                    total_amount.setText(Rs.getString("total_amount"));
                    total_tax.setText(Rs.getString("total_tax"));
                    discount.setText(Rs.getString("discount"));
                    net_amount.setText(Rs.getString("net_amount")); 
                    amount.setText(Rs.getString("net_amount"));
                    pst=conn.prepareStatement(SQL);
                    RS=pst.executeQuery();
                    while(RS.next()){
                        model.addRow(new Object[] {RS.getString("sl_no"),RS.getString("item_name"),RS.getString("rate"),RS.getString("GST"),RS.getString("qty"),RS.getString("total")});
                    }
                    invoice_table.setModel(model);
                }
                else
                    JOptionPane.showMessageDialog(null, "Not Found");
            }catch(Exception ex){
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_openActionPerformed

