    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        String item_naam=item_list.getSelectedValue();
        if(item_list.getSelectedIndex()!=-1) {
            String sql="select * from product where item_name='"+item_naam+"'";
            String SQL="select * from stock where item_name='"+item_naam+"'";
            try{
                pst=conn.prepareStatement(SQL);
                rs=pst.executeQuery();
                if(rs.next()){
                    if(Integer.parseInt(rs.getString("stock_available"))>0){
                        pst=conn.prepareStatement(sql);
                        rs=pst.executeQuery();
                        if(rs.next()){
                            String x=JOptionPane.showInputDialog("Enter Quantity");
                            buy_mod.addRow(new Object[]{""+i,rs.getString("item_name"),rs.getString("rate"),rs.getString("GST"),x,""+(Float.parseFloat(rs.getString("Total_MRP")))*Integer.parseInt(x) });
                            i++;
                        }
                    }
                    else
                        JOptionPane.showMessageDialog(null, "Item Not Available");
                }
            }catch(Exception ex){
                ex.printStackTrace();
            }
            buy.setModel(buy_mod);
            remove.setEnabled(true);
            clear.setEnabled(true);
            calculate.setEnabled(true);
        } 
        else
            JOptionPane.showMessageDialog(null, "No Item Selected");
    }//GEN-LAST:event_addActionPerformed

    private void calculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateActionPerformed
        // TODO add your handling code here:
        dis=dis+Double.parseDouble(JOptionPane.showInputDialog("Enter Discount"));
        double total = 0.00,net_total = 0.00;
        float GST=(float) 0.0;
        for(int i=0;i<buy.getRowCount();i++){
            total+=Double.parseDouble(buy.getValueAt(i, 5).toString());
            GST+=Float.parseFloat(buy.getValueAt(i, 3).toString());
        }
        total_tax.setText(""+GST);
        total_amount.setText(""+total);
        discount.setText(""+dis);
        net_amount.setText(""+(total-dis));
        generate_bill.setEnabled(true);
    }//GEN-LAST:event_calculateActionPerformed

