    private void item_set(){
        String sql="select * from product";
        try{
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            md.removeAllElements();
            item_mod.setRowCount(0);
            while(rs.next()){
                md.addElement(rs.getString("item_name"));
                item_mod.addRow(new Object[] {rs.getString("item_name"),rs.getString("rate"),rs.getString("GST"),rs.getString("Total_MRP")});
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
        item_list.setModel(md);
        item.setModel(item_mod);
    }

