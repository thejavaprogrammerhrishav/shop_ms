    private void set_value(){
        String sql="select * from product";
        String SQL="select * from stock";
        try{
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            pst=conn.prepareStatement(SQL);
            RS=pst.executeQuery();
            model.setRowCount(0);
            while(rs.next() && RS.next()){
                if(rs.getString("item_name").equals(RS.getString("item_name"))){
                    model.addRow(new Object[] {rs.getString("item_name"),rs.getString("company"),rs.getString("rate"),rs.getString("GST"),
                    RS.getString("stock_available"),rs.getString("Total_MRP")});
                }
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
        tab.setModel(model);
    }

