    private void set_value(){
        String sql="select * from purchase,payment where purchase.invoice_no=payment.invoice_no";
        try{
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            model.setRowCount(0);
            while(rs.next()){
               model.addRow(new Object[] {rs.getString("cus_name"),rs.getString("date"),rs.getString("invoice_no"),
               rs.getString("total_amount"),rs.getString("total_tax"),rs.getString("discount"),rs.getString("net_amount"),
               rs.getString("amount_paid"),rs.getString("balance"),rs.getString("pay_slip_no"),rs.getString("pay_mode"),
               rs.getString("pay_date")});
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
        tab.setModel(model);
    }

