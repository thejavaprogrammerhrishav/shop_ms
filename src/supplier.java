    private void printActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printActionPerformed
        // TODO add your handling code here:
        if(tab.getSelectedRow()==-1)
            JOptionPane.showMessageDialog(null, "No Row Selected");
        else{
            try {
                JasperDesign jd=JRXmlLoader.load(getClass().getResourceAsStream("/supplier_details.jrxml"));
                JasperReport jp=JasperCompileManager.compileReport(jd);
                Map mp=new HashMap();
                mp.put("sup__name", tab.getValueAt(tab.getSelectedRow(), 0));
                JasperPrint jpp=JasperFillManager.fillReport(jp, mp, conn);
                JasperViewer.viewReport(jpp, false);
            } catch (JRException ex) {
                Logger.getLogger(supplier.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_printActionPerformed


