    private void openActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openActionPerformed
        // TODO add your handling code here:
        if(tab.getSelectedRow()==-1)
            JOptionPane.showMessageDialog(null, "No Row Selected");
        else{
            String sql="select * from supplier where sup_name='"+tab.getValueAt(tab.getSelectedRow(), 0)+"'";
            try{
                pst=conn.prepareStatement(sql);
                rs=pst.executeQuery();
                if(rs.next()){
                    sup_name.setText(rs.getString("sup_name"));
                    supplier_er_naam=rs.getString("sup_name");
                    emailid.setText(rs.getString("email"));
                    contact.setText(rs.getString("contact"));
                    sup_address.setText(rs.getString("sup_address"));
                    dob.setText(rs.getString("dob"));
                    city.setText(rs.getString("city"));
                    state.setText(rs.getString("state"));
                    country.setText(rs.getString("country"));
                }
            }catch(Exception ex){
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_openActionPerformed

