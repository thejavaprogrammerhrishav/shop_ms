    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        if(isNew==true){
            String sql="INSERT INTO voucher(name, date, voucher_number, sub_total, total_tax, net_amount) "
                +"VALUES ('"+cus_name.getText()+"','"+date.getText()+"','"+voucher.getText()+"','"+sub_total.getText()+"','"+total_tax.getText()+"','"+net_amount.getText()+"')";
            try{
                pst=conn.prepareStatement(sql);
                pst.execute();
                for(int i=0;i<voucher_table.getRowCount();i++){
                    String SQL="INSERT INTO voucher_contents(voucher_number, sl_no, item_name, rate, GST, qty, total) "
                    +"VALUES ('"+voucher.getText()+"','"+voucher_table.getValueAt(i, 0)+"','"+voucher_table.getValueAt(i, 1)+"','"
                            +voucher_table.getValueAt(i, 2)+"','"+voucher_table.getValueAt(i, 3)+"','"+voucher_table.getValueAt(i, 4)+"','"
                            +voucher_table.getValueAt(i, 5)+"')";
                    pst=conn.prepareStatement(SQL);
                    pst.execute();
                }
                JOptionPane.showMessageDialog(null, "New Voucher Added");
            }catch(Exception ex){
                ex.printStackTrace();
            }
        }
        else
            JOptionPane.showMessageDialog(null, "Not A New Voucher");
       isNew=false;
    }//GEN-LAST:event_saveActionPerformed

