    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        String item=JOptionPane.showInputDialog("Enter Item Name");
        if(item.equals(""))
            JOptionPane.showMessageDialog(null, "Enter A Valid Item");
        else{
            String sql="select * from product where item_name='"+item+"'";
            try{
                pst=conn.prepareStatement(sql);
                rs=pst.executeQuery();
                if(rs.next()){
                    String x=JOptionPane.showInputDialog("Enter Quantity");
                    double y=Double.parseDouble(rs.getString("Total_MRP"))*Double.parseDouble(x);
                    int a;
                    try{
                        a=(Integer.parseInt(voucher_table.getValueAt(voucher_table.getRowCount()-1, 0).toString())+1);
                    }catch(Exception ex){
                        a=1;
                    }
                    model.addRow(new Object[] {""+a,
                    rs.getString("item_name"),rs.getString("rate"),rs.getString("GST"),x,y});
                }
                else
                    JOptionPane.showMessageDialog(null, "No Such Item Found");
            }catch(Exception ex){
                ex.printStackTrace();
            }
            voucher_table.setModel(model);
        }
    }//GEN-LAST:event_addActionPerformed

