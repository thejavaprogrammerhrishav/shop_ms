            JasperDesign jd=JRXmlLoader.load(getClass().getResourceAsStream("/credit_voucher.jrxml"));
            JasperReport jp=JasperCompileManager.compileReport(jd);
            Map mp=new HashMap();
            mp.put("voucher",voucher.getText());
